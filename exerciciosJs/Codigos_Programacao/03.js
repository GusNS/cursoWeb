// Desenvolva uma função que recebe dois parâmetros, um é a quantidade de horas trabalhadas por um funcionário num mês e o quanto ele recebe por hora. O retorno da função deve ser a string "Sálario igual a R$ X", em que X é o quanto o funcionário ganhou no mês.

const calcularSalario = (hrs, salHr) => {
    salario = hrs * salHr
    console.log(`Salário igual a R$ ${salario}`)
}

